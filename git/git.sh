# when getting started with git, you will need one of the following two commands

# if you are starting from scratch, git init will convert the current directory to a git repository
git init

# if you are starting from an existing repository, git clone will copy the repository to your local machine
git clone git@github.com:nagolyhprum/all-of-coding.git output-directory # if you do not provide and output directory the name of the repository will be used instead